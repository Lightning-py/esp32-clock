const picture getNumber(int number) { return *(numbers[number]); }
const picture getLetter(char letter) { return *(letters[letter - 'A']); }

const picture getSymbol(char chr) {
    int code = (int)chr;

    if (code >= 'A' && code <= 'Z') {
        return getLetter(chr);
    } else if (code >= '0' && code <= '9') {
        return getNumber(code - '0');
    } else if (chr == ':') {
        return colon;
    } else if (chr == '.') {
        return dot;
    } else if (chr == ' ') {
        return space;
    } else if (chr == '-') {
        return minus;
    } else if (chr == '`') {
        return celsium;
    } else if (chr == '~') {
        return celsium_off;
    } else {
        return zero;
    }
}

int getSymbolSizeX(char chr) {
    int code = (int)chr;

    if (code >= 'A' && code <= 'Z') {
        return getLetter(chr).x;
    } else if (code >= '0' && code <= '9') {
        return getNumber(code - '0').x;
    } else if (chr == ':') {
        return colon.x;
    } else if (chr == '.') {
        return dot.x;
    } else if (chr == ' ') {
        return space.x;
    } else if (chr == '-') {
        return minus.x;
    } else if (chr == '`') {
        return celsium.x;
    } else if (chr == '~') {
        return celsium_off.x;
    }

    throw std::invalid_argument("Invalid character");
}

int getSymbolSizeY(char chr) {
    int code = (int)chr;

    if (code >= 'A' && code <= 'Z') {
        getLetter(chr).y;
    } else if (code >= '0' && code <= '9') {
        getNumber(code - '0').y;
    } else if (chr == ':') {
        colon.y;
    } else if (chr == '.') {
        dot.y;
    } else if (chr == ' ') {
        space.y;
    } else if (chr == '-') {
        minus.y;
    } else if (chr == '`') {
        celsium.y;
    } else if (chr == '~') {
        celsium_off.y;
    }

    throw std::invalid_argument("Invalid character");
}

int getSize(std::string& str) {
    int size = 0;

    for (auto chr : str) {
        size += getSymbolSizeX(chr);
    }

    return size;
}